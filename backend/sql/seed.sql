    WHEN id BETWEEN 16 AND 20 THEN 4
    WHEN id BETWEEN 21 AND 25 THEN 5
END FROM novels;

-- 章节数据: 使用存储过程生成
DELIMITER $$
CREATE PROCEDURE generate_chapters()
BEGIN
  DECLARE done INT DEFAULT FALSE;
  DECLARE v_novel_id BIGINT;
  DECLARE v_chapter_count INT;
  DECLARE v_chapter_no INT DEFAULT 1;
  DECLARE v_content TEXT;
  DECLARE cur CURSOR FOR SELECT id FROM novels ORDER BY id;
  DECLARE CONTINUE HANDLER FOR NOT FOUND SET done = TRUE;

  OPEN cur;
  read_loop: LOOP
    FETCH cur INTO v_novel_id;
    IF done THEN LEAVE read_loop; END IF;

    SET v_chapter_count = 50 + FLOOR(RAND() * 51);
    SET v_chapter_no = 1;

    WHILE v_chapter_no <= v_chapter_count DO
      SET v_content = CONCAT(
        '　　夜深了，窗外的风吹动着窗帘，月光如水般倾泻进来。他坐在书桌前，台灯的光在纸上投下温暖的黄色光晕。手中的笔在纸上游走，写下那些无法用言语表达的情感。文字有一种神奇的力量，它能穿越时间和空间，把一个人的内心完完整整地呈现给另一个素未谋面的人。不知写了多久，窗外的天边已经泛起了鱼肚白。新的一天即将开始，而他在文字中找到了内心的平静。',

        '\n\n　　', CASE FLOOR(RAND() * 4)
          WHEN 0 THEN '春天的阳光洒满了整条街道，两旁的梧桐树抽出了嫩绿的新芽。她沿着这条熟悉的路慢慢走着，看着来来往往的行人。这座城市每天都有新的故事在发生，而她只是其中一个微小的组成部分。'
          WHEN 1 THEN '秋天的风带着凉意，吹落了满树的黄叶。公园里的湖面泛着微波，倒映着天空中飘过的云朵。她坐在湖边的长椅上，看着一对老夫妻手牵手走过。不知道他们一起经历了多少风雨，才能在这暮年之时依然如此恩爱。'
          WHEN 2 THEN '夏天的暴雨来得毫无征兆。原本晴好的天空突然乌云密布，豆大的雨点噼里啪啦地砸下来。街上的行人纷纷跑进路边的商店避雨。只有他一个人站在雨中，任由雨水打湿了全身。'
          ELSE '深夜的城市有一种独特的美。远处的霓虹灯闪烁不定，把夜空染成了五颜六色。高架桥上偶尔驶过的车辆打破了夜的寂静。他站在天台上，俯瞰着这座不眠的城市。每个人都有自己的故事，每个窗口后面都藏着一个不为人知的秘密。'
        END,

        '\n\n　　故事还在继续。每一个角色都在这命运的漩涡中挣扎、成长、蜕变。他们的命运交织在一起，编织出一幅绚丽多彩的画卷。而在这画卷的背后，是作者倾注的心血和对人性的深入思考。也许正如书中那个智者所说的那样——真正的力量并非来自于外界的认可，而是来自于内心的坚定和对梦想的不懈追求。无论前方有多少艰难险阻，只要心中有光，就永远有前进的方向。'
      );

      INSERT INTO chapters (novel_id, title, content, word_count, chapter_no)
      VALUES (v_novel_id, CONCAT('第', v_chapter_no, '章'), v_content, CHAR_LENGTH(v_content), v_chapter_no);

      SET v_chapter_no = v_chapter_no + 1;
    END WHILE;

    UPDATE novels SET word_count = (SELECT COALESCE(SUM(word_count), 0) FROM chapters WHERE novel_id = v_novel_id)
    WHERE id = v_novel_id;

  END LOOP;
  CLOSE cur;
END$$
DELIMITER ;

CALL generate_chapters();
DROP PROCEDURE IF EXISTS generate_chapters;
